//break and continue

public class T16{
	
	public static void main(String[] args) {
		
		
		
			for(int i =9; i>=0; i--) {
				
				for(int k =i;k>=0;k--) {
					if(k == 6) {
						break;
					}
					
					System.out.print(k + " ");
					
				}
			
			
			System.out.println();
				
			
			}
			
			System.out.println();
			
			for(int a =9; a>=0; a--) {
				
				for(int b =a;b>=0;b--) {
					if(b == 6) {
						continue;
					}
					
					System.out.print(b + " ");
					
				}
			
			
			System.out.println();
				
			
			}
		
		
		
		
		
		
	}
}
	
